package pompei.maths.collada.core;

public class Triangle {
  public int p1, n1, p2, n2, p3, n3;

  public Triangle() {}

  public Triangle(int p1, int n1, int p2, int n2, int p3, int n3) {
    this.p1 = p1;
    this.n1 = n1;
    this.p2 = p2;
    this.n2 = n2;
    this.p3 = p3;
    this.n3 = n3;
  }
}
